package rpg.utilities.json.classes.index;

public class Path {

	// definitions

	private String path;
	private String type;
	private String clas;

	// getters and setters

	public String getPath() {
		return this.path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getType() {
		return this.type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getClas() {
		return this.clas;
	}

	public void setClas(String clas) {
		this.clas = clas;
	}
}
